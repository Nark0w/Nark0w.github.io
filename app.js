    function translateString(value){
      const text=String(value);
      if(englishTranslations[text]!==undefined)return englishTranslations[text];
      for(const [pattern,replacer] of englishPatterns){const match=text.match(pattern);if(match)return replacer(match)}
      return text
    }
    function translatePreservingWhitespace(value){
      const match=String(value).match(/^(\s*)([\s\S]*?)(\s*)$/);if(!match)return value;
      return match[1]+translateString(match[2])+match[3]
    }
    function shouldTranslateTextNode(node){const parent=node.parentElement;return parent&&!/^(SCRIPT|STYLE|NOSCRIPT)$/.test(parent.tagName)&&node.nodeValue.trim()!==""}
    function processTextNode(node){
      if(!shouldTranslateTextNode(node))return;
      if(!originalTextNodes.has(node))originalTextNodes.set(node,node.nodeValue);
      const original=originalTextNodes.get(node);
      node.nodeValue=currentLanguage==="en"?translatePreservingWhitespace(original):original
    }
    function processElementAttributes(element){
      if(!(element instanceof Element))return;
      const attributes=["alt","title","aria-label","placeholder"];
      let originals=originalAttributes.get(element);if(!originals){originals={};originalAttributes.set(element,originals)}
      attributes.forEach(attribute=>{
        if(!element.hasAttribute(attribute))return;
        if(originals[attribute]===undefined)originals[attribute]=element.getAttribute(attribute);
        element.setAttribute(attribute,currentLanguage==="en"?translateString(originals[attribute]):originals[attribute])
      })
    }
    function walkAndTranslate(root=document.body){
      if(!root)return;
      if(root.nodeType===Node.TEXT_NODE){processTextNode(root);return}
      if(root.nodeType===Node.ELEMENT_NODE)processElementAttributes(root);
      const walker=document.createTreeWalker(root,NodeFilter.SHOW_ELEMENT|NodeFilter.SHOW_TEXT);
      let node;while((node=walker.nextNode())){if(node.nodeType===Node.TEXT_NODE)processTextNode(node);else processElementAttributes(node)}
    }
